const path = require('path');
const fs = require('fs');
const { DefaultSidebarItemsGenerator, CategoryMetadataFilenamePattern } = require('./node_modules/@docusaurus/plugin-content-docs/lib/sidebars/generator');
const { DefaultNumberPrefixParser } = require('./node_modules/@docusaurus/plugin-content-docs/lib/numberPrefix');
const { isCategoryIndex } = require('./node_modules/@docusaurus/plugin-content-docs/lib/docs');
const globby = require('globby');
const grayMatter = require('gray-matter');

async function run() {
    const docsDir = path.join(process.cwd(), 'docs');
    const autogenDir = 'products';
    const absoluteAutogenDir = path.join(docsDir, autogenDir);

    // 1. Find all docs
    const docFiles = await globby(['**/*.md', '**/*.mdx'], { cwd: docsDir });
    
    const docs = docFiles.map(file => {
        const absolutePath = path.join(docsDir, file);
        const content = fs.readFileSync(absolutePath, 'utf-8');
        const { data: frontMatter, content: body } = grayMatter(content);
        
        const sourceDirName = path.dirname(file).replace(/\\/g, '/');
        const fileName = path.basename(file, path.extname(file));
        
        // Simplified ID generation similar to Docusaurus
        const id = sourceDirName === '.' ? fileName : `${sourceDirName}/${fileName}`;

        return {
            id,
            source: file,
            sourceDirName,
            title: frontMatter.title || fileName,
            frontMatter,
            sidebarPosition: frontMatter.sidebar_position,
        };
    });

    // 2. Find all category metadata files
    const categoryMetadataFiles = await globby(['**/_category_.{json,yml,yaml}'], { cwd: docsDir });
    const categoriesMetadata = {};
    categoryMetadataFiles.forEach(file => {
        const absolutePath = path.join(docsDir, file);
        const content = fs.readFileSync(absolutePath, 'utf-8');
        const dirName = path.dirname(file).replace(/\\/g, '/');
        
        let data;
        const cleanContent = content.replace(/^\uFEFF/, '');
        if (file.endsWith('.json')) {
            try {
                data = JSON.parse(cleanContent);
            } catch (e) {
                console.error(`Error parsing ${file}: ${e.message}`);
                data = {};
            }
        } else {
            // Very basic YAML parser for this task if needed, but let's assume JSON for now or use a lib if we must
            // Actually many Docusaurus sites use JSON for _category_.json
            try {
                data = JSON.parse(content);
            } catch (e) {
                // If not JSON, could be YAML. For now just ignore or print error
                data = {};
            }
        }
        categoriesMetadata[dirName] = data;
    });

    // 3. Invoke the generator
    const rawResult = DefaultSidebarItemsGenerator({
        numberPrefixParser: DefaultNumberPrefixParser,
        isCategoryIndex: ({ fileName, directories }) => {
            const eligibleDocIndexNames = ['index', 'readme', directories[0]?.toLowerCase()];
            return eligibleDocIndexNames.includes(fileName.toLowerCase());
        },
        docs,
        item: { type: 'autogenerated', dirName: autogenDir },
        categoriesMetadata,
    });

    // 4. Simplify doc items to strings
    function simplify(items) {
        return items.map(item => {
            if (item.type === 'doc') {
                return item.id;
            }
            if (item.type === 'category') {
                return {
                    ...item,
                    items: simplify(item.items)
                };
            }
            return item;
        });
    }

    const result = simplify(rawResult);

    console.log(JSON.stringify(result, null, 2));
}

run().catch(err => {
    console.error(err);
    process.exit(1);
});
